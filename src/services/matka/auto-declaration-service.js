import { createLogger } from '../../utils/logger.js';
import { MatkaMarketModel } from '../../models/matka-market-model.js';
import { MatkaMarketResultModel } from '../../models/matka-market-result-model.js';
import { createMatkaAuditService } from './matka-audit-service.js';

const logger = createLogger('auto-declaration');

export function createAutoDeclarationService({ timezone = 'Asia/Kolkata' } = {}) {
  const auditService = createMatkaAuditService();
  let intervalId = null;

  // Generate random 3-digit panel number
  function generateRandomPanel() {
    return String(Math.floor(Math.random() * 1000)).padStart(3, '0');
  }

  // Generate Jodi from open and close panels
  function calculateJodi(openPanel, closePanel) {
    if (!openPanel || !closePanel) return null;
    
    const openSum = openPanel.split('').reduce((sum, digit) => sum + parseInt(digit), 0);
    const closeSum = closePanel.split('').reduce((sum, digit) => sum + parseInt(digit), 0);
    
    const openJodi = openSum % 10;
    const closeJodi = closeSum % 10;
    
    return `${openJodi}${closeJodi}`;
  }

  // Check if current time is 1 minute before result time
  function isOneMinuteBefore(currentTime, resultTime) {
    const current = new Date(currentTime);
    const result = new Date(resultTime);
    const oneMinuteBefore = new Date(result.getTime() - 60000); // 1 minute = 60000ms
    
    return current >= oneMinuteBefore && current < result;
  }

  // Parse time string (e.g., "2:15 PM") to today's date
  function parseTimeToToday(timeStr) {
    const today = new Date();
    const [time, period] = timeStr.split(' ');
    const [hours, minutes] = time.split(':').map(Number);
    
    let hour24 = hours;
    if (period && period.toUpperCase() === 'PM' && hours !== 12) {
      hour24 += 12;
    } else if (period && period.toUpperCase() === 'AM' && hours === 12) {
      hour24 = 0;
    }
    
    const resultTime = new Date(today);
    resultTime.setHours(hour24, minutes, 0, 0);
    
    return resultTime;
  }

  // Check and auto-declare results for all markets
  async function checkAndAutoDeclare() {
    try {
      const markets = await MatkaMarketModel.find({ isActive: true });
      const now = new Date();
      const todayStr = now.toISOString().split('T')[0];

      for (const market of markets) {
        // Check open time
        if (market.openTime) {
          const openTime = parseTimeToToday(market.openTime);
          if (isOneMinuteBefore(now, openTime)) {
            await handleAutoDeclare(market, 'open', todayStr, openTime);
          }
        }

        // Check close time
        if (market.closeTime) {
          const closeTime = parseTimeToToday(market.closeTime);
          if (isOneMinuteBefore(now, closeTime)) {
            await handleAutoDeclare(market, 'close', todayStr, closeTime);
          }
        }
      }
    } catch (error) {
      logger.error('Error in auto-declaration check:', error);
    }
  }

  // Handle auto-declaration for a specific market and session
  async function handleAutoDeclare(market, session, dateStr, resultTime) {
    try {
      // Check if result already exists for today
      let existingResult = await MatkaMarketResultModel.findOne({
        marketId: market._id,
        date: dateStr
      });

      // Check if this session already has a result
      const sessionField = session === 'open' ? 'openPanel' : 'closePanel';
      if (existingResult && existingResult[sessionField]) {
        // Already declared, skip
        return;
      }

      // Generate random panel
      const randomPanel = generateRandomPanel();
      
      if (!existingResult) {
        // Create new result document
        existingResult = new MatkaMarketResultModel({
          marketId: market._id,
          date: dateStr,
          [sessionField]: randomPanel,
          autoGenerated: {
            [session]: true
          },
          revealedAt: resultTime
        });
      } else {
        // Update existing result
        existingResult[sessionField] = randomPanel;
        existingResult.autoGenerated = {
          ...existingResult.autoGenerated,
          [session]: true
        };
        if (!existingResult.revealedAt) {
          existingResult.revealedAt = resultTime;
        }
      }

      // Calculate Jodi if both panels are available
      if (existingResult.openPanel && existingResult.closePanel) {
        existingResult.jodi = calculateJodi(existingResult.openPanel, existingResult.closePanel);
      }

      await existingResult.save();

      // Log audit trail
      await auditService.logAction({
        action: 'AUTO_DECLARE_RESULT',
        userId: 'system',
        username: 'Auto-Declaration System',
        details: {
          marketId: market._id.toString(),
          marketName: market.name,
          session,
          panel: randomPanel,
          date: dateStr,
          declaredAt: new Date().toISOString()
        },
        ipAddress: '127.0.0.1',
        userAgent: 'Auto-Declaration-Service/1.0'
      });

      logger.info(`Auto-declared ${session} result for ${market.name}:`, {
        panel: randomPanel,
        date: dateStr,
        market: market.name
      });

    } catch (error) {
      logger.error(`Error auto-declaring ${session} result for market ${market.name}:`, error);
    }
  }

  // Get auto-declared results for admin preview
  async function getAutoDeclaredResults(marketId, date) {
    try {
      const result = await MatkaMarketResultModel.findOne({
        marketId,
        date,
        $or: [
          { 'autoGenerated.open': true },
          { 'autoGenerated.close': true }
        ]
      });

      return result ? {
        openPanel: result.autoGenerated?.open ? result.openPanel : null,
        closePanel: result.autoGenerated?.close ? result.closePanel : null,
        jodi: result.jodi,
        isAutoGenerated: {
          open: result.autoGenerated?.open || false,
          close: result.autoGenerated?.close || false
        }
      } : null;
    } catch (error) {
      logger.error('Error getting auto-declared results:', error);
      return null;
    }
  }

  // Override auto-declared result with admin input
  async function overrideAutoResult(marketId, date, session, panel, adminUserId, adminUsername) {
    try {
      const result = await MatkaMarketResultModel.findOne({
        marketId,
        date
      });

      if (!result) {
        throw new Error('Result not found');
      }

      const sessionField = session === 'open' ? 'openPanel' : 'closePanel';
      const oldPanel = result[sessionField];
      
      result[sessionField] = panel;
      result.autoGenerated = {
        ...result.autoGenerated,
        [session]: false
      };

      // Recalculate Jodi if both panels are available
      if (result.openPanel && result.closePanel) {
        result.jodi = calculateJodi(result.openPanel, result.closePanel);
      }

      await result.save();

      // Log audit trail
      await auditService.logAction({
        action: 'OVERRIDE_AUTO_RESULT',
        userId: adminUserId,
        username: adminUsername,
        details: {
          marketId: marketId.toString(),
          session,
          oldPanel,
          newPanel: panel,
          date,
          overriddenAt: new Date().toISOString()
        }
      });

      logger.info(`Admin ${adminUsername} overrode auto-declared ${session} result:`, {
        marketId,
        session,
        oldPanel,
        newPanel: panel,
        date
      });

      return result;
    } catch (error) {
      logger.error('Error overriding auto result:', error);
      throw error;
    }
  }

  // Start the auto-declaration service
  function start(intervalMs = 30000) { // Check every 30 seconds
    if (intervalId) {
      logger.warn('Auto-declaration service already running');
      return;
    }

    logger.info('Starting auto-declaration service', { intervalMs });
    
    // Run initial check
    checkAndAutoDeclare();
    
    // Set up interval
    intervalId = setInterval(checkAndAutoDeclare, intervalMs);
  }

  // Stop the auto-declaration service
  function stop() {
    if (intervalId) {
      clearInterval(intervalId);
      intervalId = null;
      logger.info('Auto-declaration service stopped');
    }
  }

  return {
    start,
    stop,
    checkAndAutoDeclare,
    getAutoDeclaredResults,
    overrideAutoResult,
    generateRandomPanel,
    calculateJodi
  };
}